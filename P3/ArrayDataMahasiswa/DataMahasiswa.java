package P3.ArrayDataMahasiswa;

public class DataMahasiswa {
   public String nama;
   public String nim;
   public char jenisKelamin;
   public double ipk;

   public DataMahasiswa(String nama, String nim, char jenisKelamin, double ipk) {
      this.nama = nama;
      this.nim = nim;
      this.jenisKelamin = jenisKelamin;
      this.ipk = ipk;
   }
}